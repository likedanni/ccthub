-- 创建中国省市区数据表
CREATE TABLE IF NOT EXISTS china_regions (
    id BIGINT PRIMARY KEY AUTO_INCREMENT,
    code VARCHAR(20) NOT NULL COMMENT '区域代码',
    name VARCHAR(50) NOT NULL COMMENT '区域名称',
    parent_code VARCHAR(20) DEFAULT NULL COMMENT '父级代码',
    level TINYINT NOT NULL COMMENT '层级: 1-省 2-市 3-区',
    create_time DATETIME DEFAULT CURRENT_TIMESTAMP,
    INDEX idx_parent_code (parent_code),
    INDEX idx_level (level),
    UNIQUE KEY uk_code (code)
) ENGINE = InnoDB DEFAULT CHARSET = utf8mb4 COMMENT = '中国省市区数据表';
